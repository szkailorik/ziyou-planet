export function teachingMeaning(entry: CharacterEntry) {
  const reviewed = SIMPLE_MEANINGS[entry.char];
  if (reviewed) return reviewed;
  const generated = GENERATED_MEANINGS[entry.char]?.meaning;
  if (generated) return generated;
  if (entry.words.length > 0) {
    return `这个字常用在“${entry.words.slice(0, 2).join('”“')}”里，放进词语和句子就更容易理解。`;
  }
  return '这是课文和阅读中会遇到的字，先记住它的读音，再放进句子里理解。';
}
